package org.sciplore.beans;

import org.sciplore.formatter.CollectionBean;

public class Occurences extends CollectionBean{
	
	private String href;

	/**
	 * @param href the href to set
	 */
	public void setHref(String href) {
		this.href = href;
	}

	/**
	 * @return the href
	 */
	public String getHref() {
		return href;
	}
	
	

}
